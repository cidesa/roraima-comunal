    {
      $prefijo = substr($m,0,3);
    $metodo = strtolower(substr($m,3));
        if($prefijo=='get'){
      if(isset($this->$metodo)) return $this->$metodo;
      else return '';
    }elseif($prefijo=='set'){
      if(isset($this->$metodo)) $this->$metodo = $a[0];
    }else call_user_func_array($m, $a);

    }

	
	public function delete($con = null)
	{
		if ($this->isDeleted()) {
			throw new PropelException("This object has already been deleted.");
		}

		if ($con === null) {
			$con = Propel::getConnection(CamotfalPeer::DATABASE_NAME);
		}

		try {
			$con->begin();
			CamotfalPeer::doDelete($this, $con);
			$this->setDeleted(true);
			$con->commit();
		} catch (PropelException $e) {
			$con->rollback();
			throw $e;
		}
	}

	
	public function save($con = null)
	{
		if ($this->isDeleted()) {
			throw new PropelException("You cannot save an object that has been deleted.");
		}

		if ($con === null) {
			$con = Propel::getConnection(CamotfalPeer::DATABASE_NAME);
		}

		try {
			$con->begin();
			$affectedRows = $this->doSave($con);
			$con->commit();
			return $affectedRows;
		} catch (PropelException $e) {
			$con->rollback();
			throw $e;
		}
	}

	
	protected function doSave($con)
	{
		$affectedRows = 0; 		if (!$this->alreadyInSave) {
			$this->alreadyInSave = true;


						if ($this->isModified()) {
				if ($this->isNew()) {
					$pk = CamotfalPeer::doInsert($this, $con);
					$affectedRows += 1; 										 										 
					$this->setId($pk);  
					$this->setNew(false);
				} else {
					$affectedRows += CamotfalPeer::doUpdate($this, $con);
				}
				$this->resetModified(); 			}

			if ($this->collCaartrcps !== null) {
				foreach($this->collCaartrcps as $referrerFK) {
					if (!$referrerFK->isDeleted()) {
						$affectedRows += $referrerFK->save($con);
					}
				}
			}

			$this->alreadyInSave = false;
		}
		return $affectedRows;
	} 
	
	protected $validationFailures = array();

	
	public function getValidationFailures()
	{
		return $this->validationFailures;
	}

	
	public function validate($columns = null)
	{
		$res = $this->doValidate($columns);
		if ($res === true) {
			$this->validationFailures = array();
			return true;
		} else {
			$this->validationFailures = $res;
			return false;
		}
	}

	
	protected function doValidate($columns = null)
	{
		if (!$this->alreadyInValidation) {
			$this->alreadyInValidation = true;
			$retval = null;

			$failureMap = array();


			if (($retval = CamotfalPeer::doValidate($this, $columns)) !== true) {
				$failureMap = array_merge($failureMap, $retval);
			}


				if ($this->collCaartrcps !== null) {
					foreach($this->collCaartrcps as $referrerFK) {
						if (!$referrerFK->validate($columns)) {
							$failureMap = array_merge($failureMap, $referrerFK->getValidationFailures());
						}
					}
				}


			$this->alreadyInValidation = false;
		}

		return (!empty($failureMap) ? $failureMap : true);
	}

	
	public function getByName($name, $type = BasePeer::TYPE_PHPNAME)
	{
		$pos = CamotfalPeer::translateFieldName($name, $type, BasePeer::TYPE_NUM);
		return $this->getByPosition($pos);
	}

	
	public function getByPosition($pos)
	{
		switch($pos) {
			case 0:
				return $this->getCodfal();
				break;
			case 1:
				return $this->getDesfal();
				break;
			case 2:
				return $this->getTipfal();
				break;
			case 3:
				return $this->getId();
				break;
			default:
				return null;
				break;
		} 	}

	
	public function toArray($keyType = BasePeer::TYPE_PHPNAME)
	{
		$keys = CamotfalPeer::getFieldNames($keyType);
		$result = array(
			$keys[0] => $this->getCodfal(),
			$keys[1] => $this->getDesfal(),
			$keys[2] => $this->getTipfal(),
			$keys[3] => $this->getId(),
		);
		return $result;
	}

	
	public function setByName($name, $value, $type = BasePeer::TYPE_PHPNAME)
	{
		$pos = CamotfalPeer::translateFieldName($name, $type, BasePeer::TYPE_NUM);
		return $this->setByPosition($pos, $value);
	}

	
	public function setByPosition($pos, $value)
	{
		switch($pos) {
			case 0:
				$this->setCodfal($value);
				break;
			case 1:
				$this->setDesfal($value);
				break;
			case 2:
				$this->setTipfal($value);
				break;
			case 3:
				$this->setId($value);
				break;
		} 	}

	
	public function fromArray($arr, $keyType = BasePeer::TYPE_PHPNAME)
	{
		$keys = CamotfalPeer::getFieldNames($keyType);

		if (array_key_exists($keys[0], $arr)) $this->setCodfal($arr[$keys[0]]);
		if (array_key_exists($keys[1], $arr)) $this->setDesfal($arr[$keys[1]]);
		if (array_key_exists($keys[2], $arr)) $this->setTipfal($arr[$keys[2]]);
		if (array_key_exists($keys[3], $arr)) $this->setId($arr[$keys[3]]);
	}

	
	public function buildCriteria()
	{
		$criteria = new Criteria(CamotfalPeer::DATABASE_NAME);

		if ($this->isColumnModified(CamotfalPeer::CODFAL)) $criteria->add(CamotfalPeer::CODFAL, $this->codfal);
		if ($this->isColumnModified(CamotfalPeer::DESFAL)) $criteria->add(CamotfalPeer::DESFAL, $this->desfal);
		if ($this->isColumnModified(CamotfalPeer::TIPFAL)) $criteria->add(CamotfalPeer::TIPFAL, $this->tipfal);
		if ($this->isColumnModified(CamotfalPeer::ID)) $criteria->add(CamotfalPeer::ID, $this->id);

		return $criteria;
	}

	
	public function buildPkeyCriteria()
	{
		$criteria = new Criteria(CamotfalPeer::DATABASE_NAME);

		$criteria->add(CamotfalPeer::CODFAL, $this->codfal);
		$criteria->add(CamotfalPeer::ID, $this->id);

		return $criteria;
	}

	
	public function getPrimaryKey()
	{
		$pks = array();

		$pks[0] = $this->getCodfal();

		$pks[1] = $this->getId();

		return $pks;
	}

	
	public function setPrimaryKey($keys)
	{

		$this->setCodfal($keys[0]);

		$this->setId($keys[1]);

	}

	
	public function copyInto($copyObj, $deepCopy = false)
	{

		$copyObj->setDesfal($this->desfal);

		$copyObj->setTipfal($this->tipfal);


		if ($deepCopy) {
									$copyObj->setNew(false);

			foreach($this->getCaartrcps() as $relObj) {
				$copyObj->addCaartrcp($relObj->copy($deepCopy));
			}

		} 

		$copyObj->setNew(true);

		$copyObj->setCodfal(NULL); 
		$copyObj->setId(NULL); 
	}

	
	public function copy($deepCopy = false)
	{
				$clazz = get_class($this);
		$copyObj = new $clazz();
		$this->copyInto($copyObj, $deepCopy);
		return $copyObj;
	}

	
	public function getPeer()
	{
		if (self::$peer === null) {
			self::$peer = new CamotfalPeer();
		}
		return self::$peer;
	}

	
	public function initCaartrcps()
	{
		if ($this->collCaartrcps === null) {
			$this->collCaartrcps = array();
		}
	}

	
	public function getCaartrcps($criteria = null, $con = null)
	{
				include_once 'lib/model/om/BaseCaartrcpPeer.php';
		if ($criteria === null) {
			$criteria = new Criteria();
		}
		elseif ($criteria instanceof Criteria)
		{
			$criteria = clone $criteria;
		}

		if ($this->collCaartrcps === null) {
			if ($this->isNew()) {
			   $this->collCaartrcps = array();
			} else {

				$criteria->add(CaartrcpPeer::CODFAL, $this->getCodfal());

				CaartrcpPeer::addSelectColumns($criteria);
				$this->collCaartrcps = CaartrcpPeer::doSelect($criteria, $con);
			}
		} else {
						if (!$this->isNew()) {
												

				$criteria->add(CaartrcpPeer::CODFAL, $this->getCodfal());

				CaartrcpPeer::addSelectColumns($criteria);
				if (!isset($this->lastCaartrcpCriteria) || !$this->lastCaartrcpCriteria->equals($criteria)) {
					$this->collCaartrcps = CaartrcpPeer::doSelect($criteria, $con);
				}
			}
		}
		$this->lastCaartrcpCriteria = $criteria;
		return $this->collCaartrcps;
	}

	
	public function countCaartrcps($criteria = null, $distinct = false, $con = null)
	{
				include_once 'lib/model/om/BaseCaartrcpPeer.php';
		if ($criteria === null) {
			$criteria = new Criteria();
		}
		elseif ($criteria instanceof Criteria)
		{
			$criteria = clone $criteria;
		}

		$criteria->add(CaartrcpPeer::CODFAL, $this->getCodfal());

		return CaartrcpPeer::doCount($criteria, $distinct, $con);
	}

	
	public function addCaartrcp(Caartrcp $l)
	{
		$this->collCaartrcps[] = $l;
		$l->setCamotfal($this);
	}

} 