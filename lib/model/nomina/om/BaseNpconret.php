	public function buildCriteria()
	{
		$criteria = new Criteria(NpconretPeer::DATABASE_NAME);

		if ($this->isColumnModified(NpconretPeer::CODEMP)) $criteria->add(NpconretPeer::CODEMP, $this->codemp);
		if ($this->isColumnModified(NpconretPeer::CODCON)) $criteria->add(NpconretPeer::CODCON, $this->codcon);
		if ($this->isColumnModified(NpconretPeer::FECINI)) $criteria->add(NpconretPeer::FECINI, $this->fecini);
		if ($this->isColumnModified(NpconretPeer::FECCIE)) $criteria->add(NpconretPeer::FECCIE, $this->feccie);
		if ($this->isColumnModified(NpconretPeer::MONTO)) $criteria->add(NpconretPeer::MONTO, $this->monto);
		if ($this->isColumnModified(NpconretPeer::ID)) $criteria->add(NpconretPeer::ID, $this->id);

		return $criteria;
	}

	
