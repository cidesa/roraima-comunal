	{
		$criteria = new Criteria(DfdiaferPeer::DATABASE_NAME);

		if ($this->isColumnModified(DfdiaferPeer::DIAFER)) $criteria->add(DfdiaferPeer::DIAFER, $this->diafer);
		if ($this->isColumnModified(DfdiaferPeer::ID)) $criteria->add(DfdiaferPeer::ID, $this->id);

		return $criteria;
	}

	
