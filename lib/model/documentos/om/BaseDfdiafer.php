  {
    try {

      $this->diafer = $rs->getDate($startcol + 0, null);

      $this->id = $rs->getInt($startcol + 1);

      $this->resetModified();

      $this->setNew(false);

      $this->afterHydrate();

            return $startcol + 2; 
    } catch (Exception $e) {
      throw new PropelException("Error populating Dfdiafer object", $e);
    }
  }


  protected function afterHydrate()
  {

  }
    
  
