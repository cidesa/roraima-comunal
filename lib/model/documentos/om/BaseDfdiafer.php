	{
		switch($pos) {
			case 0:
				$this->setDiafer($value);
				break;
			case 1:
				$this->setId($value);
				break;
		} 	}

	
	public function fromArray($arr, $keyType = BasePeer::TYPE_PHPNAME)
	{
		$keys = DfdiaferPeer::getFieldNames($keyType);

		if (array_key_exists($keys[0], $arr)) $this->setDiafer($arr[$keys[0]]);
		if (array_key_exists($keys[1], $arr)) $this->setId($arr[$keys[1]]);
	}

	
