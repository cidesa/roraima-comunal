<?php


abstract class BaseDfdiafer extends BaseObject  implements Persistent {


	
	protected static $peer;


	
	protected $diafer;


	
	protected $id;

	
	protected $alreadyInSave = false;

	
	protected $alreadyInValidation = false;

  
  public function getDiafer($format = 'Y-m-d')
  {

    if ($this->diafer === null || $this->diafer === '') {
      return null;
    } elseif (!is_int($this->diafer)) {
            $ts = adodb_strtotime($this->diafer);
      if ($ts === -1 || $ts === false) {         throw new PropelException("Unable to parse value of [diafer] as date/time value: " . var_export($this->diafer, true));
      }
    } else {
      $ts = $this->diafer;
    }
    if ($format === null) {
      return $ts;
    } elseif (strpos($format, '%') !== false) {
      return adodb_strftime($format, $ts);
    } else {
      return @adodb_date($format, $ts);
    }
  }

  
  public function getId()
  {

    return $this->id;

  }
	
	public function setDiafer($v)
	{

		if (is_array($v)){
        	$value_array = $v;
        	$v = (isset($value_array['hour']) ? ' '.$value_array['hour'].':'.$value_array['minute'].(isset($value_array['second']) ? ':'.$value_array['second'] : '') : '');
		}

    if ($v !== null && !is_int($v)) {
      $ts = adodb_strtotime($v);
      if ($ts === -1 || $ts === false) {         throw new PropelException("Unable to parse date/time value for [diafer] from input: " . var_export($v, true));
      }
    } else {
      $ts = $v;
    }
    if ($this->diafer !== $ts) {
      $this->diafer = $ts;
      $this->modifiedColumns[] = DfdiaferPeer::DIAFER;
    }

	} 
	
	public function setId($v)
	{

    if ($this->id !== $v) {
        $this->id = $v;
        $this->modifiedColumns[] = DfdiaferPeer::ID;
      }
  
	} 
  
